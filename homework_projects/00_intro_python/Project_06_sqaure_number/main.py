def main():
    # Prompt the user to enter a number
    num = float(input("Type a number to see its square: "))  # Convert input to float to handle decimals
    
    # Calculate and display the square of the number
    print(f"{num} squared is {num ** 2}")  # Use formatted string for clear output

# There is no need to edit code beyond this point
if __name__ == '__main__':
    main()
